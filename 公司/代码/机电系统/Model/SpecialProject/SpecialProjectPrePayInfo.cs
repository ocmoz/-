using System;
using System.Collections.Generic;
using System.Text;

namespace FM2E.Model.SpecialProject
{
    /// <summary>
    /// 专项工程预支付项实体类
    /// </summary>
    public class SpecialProjectPrePayInfo
    {
        public SpecialProjectPrePayInfo()
		{}
		#region Model
		private long _projectid;
        private long _itemid;
        private string _itemname = "";
        private DateTime _time = DateTime.MinValue;
		private decimal _amount;
        private string _method = "";
		private decimal _paid;
        private string _payee = "";
        private string _remark = "";
		/// <summary>
		/// 专项工程ID
		/// </summary>
        public long ProjectID
		{
			set{ _projectid=value;}
			get{return _projectid;}
		}
		/// <summary>
		/// 预支付项序号
		/// </summary>
        public long ItemID
		{
			set{ _itemid=value;}
			get{return _itemid;}
		}
		/// <summary>
		/// 支付项名称
		/// </summary>
		public string ItemName
		{
			set{ _itemname=value;}
			get{return _itemname;}
		}
		/// <summary>
		/// 预定支付时间
		/// </summary>
		public DateTime Time
		{
			set{ _time=value;}
			get{return _time;}
		}
		/// <summary>
		/// 支付金额
		/// </summary>
		public decimal Amount
		{
			set{ _amount=value;}
			get{return _amount;}
		}
		/// <summary>
		/// 支付方式
		/// </summary>
		public string Method
		{
			set{ _method=value;}
			get{return _method;}
		}
		/// <summary>
		/// 已经支付金额
		/// </summary>
		public decimal Paid
		{
			set{ _paid=value;}
			get{return _paid;}
		}
		/// <summary>
		/// 支付人
		/// </summary>
		public string Payee
		{
			set{ _payee=value;}
			get{return _payee;}
		}
		/// <summary>
		/// 备注
		/// </summary>
		public string Remark
		{
			set{ _remark=value;}
			get{return _remark;}
		}
		#endregion Model
    }
}
