using System;
using System.Collections.Generic;
using System.Text;

namespace FM2E.Model.SpecialProject
{
    /// <summary>
    /// 专项工程设计方案
    /// </summary>
    public class SpecialProjectDesignInfo
    {
        public SpecialProjectDesignInfo()
		{}
		#region Model
		private long _projectid;
        private DateTime _approvaldate = DateTime.MinValue;
        private string _designname = "";
        private string _designer = "";
        private string _designerinfo = "";
        private decimal _designcost;
        private decimal _projectcost;
        private string _design = "";
        private string _attechment = "";
        private string _approvaler = "";
		private int _result;
        private string _feeback = "";
		/// <summary>
		/// 专项工程ID
		/// </summary>
        public long ProjectID
		{
			set{ _projectid=value;}
			get{return _projectid;}
		}
		/// <summary>
		/// 审批时间
		/// </summary>
		public DateTime ApprovalDate
		{
			set{ _approvaldate=value;}
			get{return _approvaldate;}
		}
		/// <summary>
		/// 设计方案名称
		/// </summary>
		public string DesignName
		{
			set{ _designname=value;}
			get{return _designname;}
		}
		/// <summary>
		/// 设计者
		/// </summary>
		public string Designer
		{
			set{ _designer=value;}
			get{return _designer;}
		}
		/// <summary>
		/// 设计者信息
		/// </summary>
		public string DesignerInfo
		{
			set{ _designerinfo=value;}
			get{return _designerinfo;}
		}
		/// <summary>
		/// 设计方案描述
		/// </summary>
		public string Design
		{
			set{ _design=value;}
			get{return _design;}
		}
		/// <summary>
		/// 设计方案附件
		/// </summary>
		public string Attechment
		{
			set{ _attechment=value;}
			get{return _attechment;}
		}
		/// <summary>
		/// 审批人
		/// </summary>
		public string Approvaler
		{
			set{ _approvaler=value;}
			get{return _approvaler;}
		}
		/// <summary>
		/// 审批结果
		/// </summary>
		public int Result
		{
			set{ _result=value;}
			get{return _result;}
		}
		/// <summary>
		/// 审批意见
		/// </summary>
		public string FeeBack
		{
			set{ _feeback=value;}
			get{return _feeback;}
		}

        /// <summary>
        /// 设计费用
        /// </summary>
        public decimal DesignCost
        {
            get { return _designcost; }
            set { _designcost = value; }
        }

        /// <summary>
        /// 设计的工程费用
        /// </summary>
        public decimal ProjectCost
        {
            get
            {
                return _projectcost;
            }
            set
            {
                _projectcost = value;
            }
        }
		#endregion Model
    }
}
