using System;
using System.Collections.Generic;
using System.Text;

namespace FM2E.Model.SpecialProject
{
    /// <summary>
    /// 专项工程施工检查记录实体类
    /// </summary>
    public class SpecialProjectCheckRecordInfo
    {
        public SpecialProjectCheckRecordInfo()
		{}
		#region Model
        private long _projectid;
        private long _itemid;
        private DateTime _checktime = DateTime.MinValue;
		private long _planitemid;
		private decimal _progress;
        private string _quality = "";
        private string _hr = "";
        private string _checker = "";
        private string _remark = "";
		/// <summary>
		/// 专项工程ID
		/// </summary>
		public long ProjectID
		{
			set{ _projectid=value;}
			get{return _projectid;}
		}
		/// <summary>
		/// 检查项序号
		/// </summary>
        public long ItemID
		{
			set{ _itemid=value;}
			get{return _itemid;}
		}
		/// <summary>
		/// 检查时间
		/// </summary>
		public DateTime CheckTime
		{
			set{ _checktime=value;}
			get{return _checktime;}
		}
		/// <summary>
		/// 检查的对应工作项序号
		/// </summary>
		public long PlanItemID
		{
			set{ _planitemid=value;}
			get{return _planitemid;}
		}
		/// <summary>
		/// 工作项进度
		/// </summary>
		public decimal Progress
		{
			set{ _progress=value;}
			get{return _progress;}
		}
		/// <summary>
		/// 质量评价
		/// </summary>
		public string Quality
		{
			set{ _quality=value;}
			get{return _quality;}
		}
		/// <summary>
		/// 人员安排情况评价
		/// </summary>
		public string HR
		{
			set{ _hr=value;}
			get{return _hr;}
		}
		/// <summary>
		/// 检查人
		/// </summary>
		public string Checker
		{
			set{ _checker=value;}
			get{return _checker;}
		}
		/// <summary>
		/// 备注
		/// </summary>
		public string Remark
		{
			set{ _remark=value;}
			get{return _remark;}
		}
		#endregion Model
    }
}
